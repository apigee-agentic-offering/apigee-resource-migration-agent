def scan_local_product_proxies() -> dict:
    """Scans transformed API Product JSON files to extract a list of required API proxies."""
    print("--- Tool: scan_local_product_proxies called ---")
    try:
        if PROJECT_ROOT not in sys.path:
            sys.path.append(PROJECT_ROOT)
        import config
        
        output_dir = getattr(config, 'OUTPUT_DIR', 'transformed_resources')
        product_dir = os.path.join(PROJECT_ROOT, output_dir, "org", "apiproducts")
        
        if not os.path.exists(product_dir):
            return {"status": "error", "message": f"Directory not found: {product_dir}. Have you transformed the products yet?"}
            
        product_files = glob.glob(os.path.join(product_dir, "*.json"))
        if not product_files:
            return {"status": "success", "proxies": [], "message": "No API Product files found."}
            
        all_proxies = set()
        for file_path in product_files:
            with open(file_path, 'r', encoding='utf-8') as f:
                try:
                    data = json.load(f)
                    all_proxies.update(data.get("proxies", []))
                except json.JSONDecodeError:
                    pass
                    
        return {
            "status": "success", 
            "proxies": sorted(list(all_proxies)),
            "count": len(all_proxies)
        }
    except Exception as e:
        return {"status": "error", "message": str(e)}

