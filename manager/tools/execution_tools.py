def run_import_script_bkp() -> dict:
    """Executes 2-import_kvm.py synchronously, blocks until finished, and extracts stats."""
    print("--- Tool: run_import_script_bkp called ---")
    try:
        script_path = os.path.join(PROJECT_ROOT, "kvm-migration-scripts", "2-import_kvm.py")
        
        # This blocks the agent until the script completely finishes
        result = subprocess.run(["python", script_path], cwd=PROJECT_ROOT)
        
        log_dir = os.path.join(PROJECT_ROOT, "run_logs", "import_kvms_run")
        if not os.path.exists(log_dir):
             return {"status": "error", "message": "Log directory not found."}

        list_of_files = glob.glob(os.path.join(log_dir, "*.log"))
        if not list_of_files:
            return {"status": "error", "message": "Script ran but no log file was generated."}
            
        latest_file = max(list_of_files, key=os.path.getctime)
        
        with open(latest_file, 'r', encoding='utf-8') as f:
            log_content = f.read()
            
        def extract_stat(label):
            match = re.search(rf"{label}\s*:\s*(\d+)", log_content)
            return int(match.group(1)) if match else 0

        stats = {
            "files_processed": extract_stat("Files Processed"),
            "maps_created": extract_stat("Maps Created"),
            "maps_synced": extract_stat("Maps Synced"),
            "entries_created": extract_stat("Entries Created"),
            "entries_updated": extract_stat("Entries Updated"),
            "files_deleted": extract_stat("Files Deleted"),
            "failures": extract_stat("Failures")
        }

        # Check for any failures or a non-zero exit code
        if result.returncode != 0 or stats["failures"] > 0:
            error_lines = [line.strip() for line in log_content.split('\n') if '❌' in line or 'Error:' in line]
            unique_errors = list(dict.fromkeys(error_lines))
            return {
                "status": "completed_with_errors", 
                "summary_data": stats,
                "error_details": unique_errors[:5]
            }

        return {"status": "completed_successfully", "summary_data": stats}
        
    except Exception as e:
        return {"status": "error", "message": str(e)}
    
